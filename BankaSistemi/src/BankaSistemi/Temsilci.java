package BankaSistemi;

public class Temsilci {
	
	private int temsilci_id;
	private String temsilci_tc;
	private String temsilci_sifre;
	private int temsilci_maas;
	
	
	public int getTemsilci_id() {
		return temsilci_id;
	}
	public void setTemsilci_id(int temsilci_id) {
		this.temsilci_id = temsilci_id;
	}
	public String getTemsilci_tc() {
		return temsilci_tc;
	}
	public void setTemsilci_tc(String temsilci_tc) {
		this.temsilci_tc = temsilci_tc;
	}
	public String getTemsilci_sifre() {
		return temsilci_sifre;
	}
	public void setTemsilci_sifre(String temsilci_sifre) {
		this.temsilci_sifre = temsilci_sifre;
	}
	public int getTemsilci_maas() {
		return temsilci_maas;
	}
	public void setTemsilci_maas(int temsilci_maas) {
		this.temsilci_maas = temsilci_maas;
	}
	

}
